% converting the above raw data to symbols
for counter = 1:length(bc_dtr)-1
   
    % when BC (SA, DT) data increases in sequential steps, register 1 else 0
    if bc_dtr(counter+1)-bc_dtr(counter)>0
        bc(counter,1) = 1;
    else bc(counter,1) = 0;
    end
    
    % when MO increases in sequential steps, register 1 else 0
    if mo0(counter+1)-mo0(counter)>0
        mo(counter,1) = 1;
    else mo(counter,1) = 0;
    end
    
    % when MS data indicates at least one mass shooting, set 1 else 0
    if ms_occ(counter)>0
        ms(counter,1) = 1;
    else ms(counter,1) = 0;
    end
    
end